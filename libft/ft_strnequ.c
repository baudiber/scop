#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (n == 0)
		return (1);
	if (s1 && s2)
	{
		if (0 == ft_strncmp((char *)s1, (char *)s2, n))
			return (1);
		else
			return (0);
	}
	return (0);
}
