#include "libft.h"

void	ft_putints(int *tab, size_t size)
{
	int		i;

	if (!tab)
		return ;
	i = 0;
	while (size--)
	{
		ft_putnbr(tab[i++]);
		write(1, "\n", 1);
	}
}
